#include <unistd.h>

void	ft_is_negative(int n)
{
	int a;
	int b;

	b = 'P';
	a = 'N';
	if (n < 0)
	{
		write(1, &a, 1);
	}
	else
	{
		write(1, &b, 1);
	}
}
