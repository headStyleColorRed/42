int		ft_isalpha(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] < 65)
			return (0);
		else if (str[i] > 122)
			return (0);
		else if (str[i] >= 91 && str[i] <= 96)
			return (0);
		i++;
	}
	return (1);
}
